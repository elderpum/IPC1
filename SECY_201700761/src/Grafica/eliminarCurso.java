    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        int x = JOptionPane.showConfirmDialog(null, "¿Desea eliminar este Curso?", "Sistema SECY", JOptionPane.YES_NO_OPTION);
        if (x == JOptionPane.YES_OPTION) {
            try{
                Listas.curso.eliminar(txtnocurso.getText());
                btncancelar.setEnabled(false);
                btneliminar.setEnabled(false);
                btnbuscar.setEnabled(true);
                txtnocurso.setText("");
                txtnombre.setText("");
                txtcatedratico.setText("");
                txtcreditos.setText("");
                txtlab.setText("");
                txtseccion.setText("");
                txtpre.setText("");
                txtpost.setText("");
            }catch (Exception e){
                System.out.println("Esto no sirve >:c");
                btncancelar.setEnabled(true);
                btneliminar.setEnabled(true);
                btnbuscar.setEnabled(false);
            }
            JOptionPane.showConfirmDialog(null, "Curso Eliminado con éxito.", "Sistema SECY", JOptionPane.OK_OPTION);
        }else{
            JOptionPane.showMessageDialog(null, "Operación Cancelada.", "Sistema SECY", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btneliminarActionPerformed

