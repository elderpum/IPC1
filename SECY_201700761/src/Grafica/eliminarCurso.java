    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        String carnet = txtnocurso.getText();
        try{
            txtnombre.setEnabled(true);
            txtcatedratico.setEnabled(true);
            txtcreditos.setEnabled(true);
            txtlab.setEnabled(true);
            txtseccion.setEnabled(true);
            txtpre.setEnabled(true);
            txtpost.setEnabled(true);
            txtnombre.setText(Listas.curso.buscarNodo(carnet).getNombreCurso());
            txtcatedratico.setText(Listas.curso.buscarNodo(carnet).getCatedratico());
            txtcreditos.setText(String.valueOf(Listas.curso.buscarNodo(carnet).getCreditos()));
            txtlab.setText(Listas.curso.buscarNodo(carnet).getLab());
            txtseccion.setText(Listas.curso.buscarNodo(carnet).getSeccion());
            txtpre.setText(Listas.curso.buscarNodo(carnet).getPrerequisito().toString().split(",").toString());
            txtpost.setText(Listas.curso.buscarNodo(carnet).getPostrequisito());
            btneliminar.setEnabled(true);
            btncancelar.setEnabled(true);
            btnbuscar.setEnabled(false);
        }catch (Exception e){
            System.out.println("Esto no funciona >:c");
            JOptionPane.showMessageDialog(null, "Curso No Encontrado", "Sistema SECY", JOptionPane.ERROR_MESSAGE);
            txtnombre.setEnabled(false);
            txtcatedratico.setEnabled(false);
            txtcreditos.setEnabled(false);
            txtlab.setEnabled(false);
            txtseccion.setEnabled(false);
            txtpre.setEnabled(false);
            txtpost.setEnabled(false);
            btncancelar.setEnabled(false);
            btneliminar.setEnabled(false);
            btnbuscar.setEnabled(true);
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

